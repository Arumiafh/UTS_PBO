    private void buttonDelete1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDelete1ActionPerformed
        int baris = tb_barang.getSelectedRow();
        if(baris != -1)
        {   String id_barang = tb_barang.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM tb_barang WHERE id_barang='"+id_barang+"'";
            int status = KoneksiDB.execute(SQL);
            
            if (status == 1)
            { JOptionPane.showMessageDialog(this, "Data berhasil dihapus",
                    "Sukses", JOptionPane.INFORMATION_MESSAGE);}
            else
            { JOptionPane.showMessageDialog(this, "Data gagal dihapus","Gagal", 
                    JOptionPane.WARNING_MESSAGE);}}
        else
        { JOptionPane.showMessageDialog(this, "Pilih Baris Data Terlebih "
                    + "Dahulu","Error",JOptionPane.WARNING_MESSAGE);}
        
    }//GEN-LAST:event_buttonDelete1ActionPerformed

