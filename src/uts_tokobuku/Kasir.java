    private void buttonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSaveActionPerformed
        
        if ("".equals(txtIdBarang.getText()) || "".equals(txtNamaBarang.getText()) || "".equals(txtHargaBarang.getText()))
        {
            JOptionPane.showMessageDialog(this, "Lengkapi Data Barang", "Error", JOptionPane.WARNING_MESSAGE);
        }
        
        String SQL = "INSERT INTO tb_barang (id_barang,nama_barang,harga_barang)"
                + "VALUES('"+txtIdBarang.getText()+"','"+txtNamaBarang.getText()+"','"+txtHargaBarang.getText()+"')";
        
        int status = KoneksiDB.execute(SQL);
        if(status == 1)
        {
            JOptionPane.showMessageDialog(this, "Data Barang berhasil ditambahkan","Sukses", JOptionPane.INFORMATION_MESSAGE);
            selectData();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Data Barang gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_buttonSaveActionPerformed

